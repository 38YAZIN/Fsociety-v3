    global sent, errors
    while time.time() < end_time:
        try:
            r = requests.get(target, timeout=1)
            with lock:
                sent += 1
            print(f"[Thread-{thread_id}] Request sent! Total: {sent}")
        except Exception as e:
            with lock:
                errors += 1
            print(f"[Thread-{thread_id}] Error: {e}")
        time.sleep(interval)

